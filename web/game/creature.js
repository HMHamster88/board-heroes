export class Creature {
    constructor(name, images, attack, defence, damage, health, speed, growth, abilities) {
      this.name = name;
      this.images = images;
      this.attack = attack;
      this.defence = defence;
      this.damage = damage;
      this.health = health;
      this.speed = speed;
      this.growth = growth;
      this.abilities = abilities;
    }
  }